package day15_ForLoop;

public class ForLoopIntro {
    public static void main(String[] args) {
        System.out.println("Hello World!");//instead of to write manually 10 times we use loop
        System.out.println("---------------------");
        for (int i = 1; i <= 10; i++) {
            System.out.println("Hello World!");
        }

    }
}
