package day09_IfStatements;

public class PosNegZero {
    public static void main(String[] args) {
        int n = 100;
        if (n > 0) {
            System.out.println("Possitive");
        } else if (n < 0) {
            System.out.println("Negative");
        } else {
            System.out.println("Zero");
        }

    }
}
